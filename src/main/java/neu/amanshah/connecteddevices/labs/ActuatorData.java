package neu.amanshah.connecteddevices.labs;

public class ActuatorData {
	
	float value_val=0;
	String command_val ="";
	String name ="";
	public float getValue_val() {
		return value_val;
	}
	public void setValue_val(float value_val) {
		this.value_val = value_val;
	}
	public String getCommand_val() {
		return command_val;
	}
	public void setCommand_val(String command_val) {
		this.command_val = command_val;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}

	
	
	
}
